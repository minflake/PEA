function formatarCPF() {
  var cpf = document.getElementById("cpf").value;

  // Remover caracteres especiais do CPF
  cpf = cpf.replace(/[^\d]+/g, '');

  // Limitar o CPF a 11 dígitos
  cpf = cpf.slice(0, 11);

  // Adicionar pontos e hífen após 3, 6 e 9 dígitos
  if (cpf.length > 3) {
    cpf = cpf.slice(0, 3) + '.' + cpf.slice(3);
  }
  if (cpf.length > 7) {
    cpf = cpf.slice(0, 7) + '.' + cpf.slice(7);
  }
  if (cpf.length > 11) {
    cpf = cpf.slice(0, 11) + '-' + cpf.slice(11);
  }

  // Atualizar valor do campo de CPF
  document.getElementById("cpf").value = cpf;
}

function validarCPF() {
  var cpf = document.getElementById("cpf").value;
  var classe_cpf = document.getElementById("cpf");

  // Remover caracteres especiais do CPF
  cpf = cpf.replace(/[^\d]+/g, '');

  // Verificar se o CPF tem 11 dígitos
  if (cpf.length !== 11) {
    classe_cpf.setAttribute("data-invalid", "true");
  }

  // Verificar se todos os dígitos são iguais
  var todosDigitosIguais = true;
  for (var i = 1; i < 11; i++) {
    if (cpf[i] !== cpf[0]) {
      todosDigitosIguais = false;
      break;
    }
  }
  if (todosDigitosIguais) {
    classe_cpf.setAttribute("data-invalid", "true");
  }

  // Validar dígitos verificadores
  var soma = 0;
  var resto;
  for (var i = 0; i < 9; i++) {
    soma += parseInt(cpf.charAt(i)) * (10 - i);
  }
  resto = (soma * 10) % 11;

  if (resto === 10 || resto === 11) {
    resto = 0;
  }

  if (resto !== parseInt(cpf.charAt(9))) {
    classe_cpf.setAttribute("data-invalid", "true");
  }

  soma = 0;
  for (var i = 0; i < 10; i++) {
    soma += parseInt(cpf.charAt(i)) * (11 - i);
  }
  resto = (soma * 10) % 11;

  if (resto === 10 || resto === 11) {
    resto = 0;
  }

  if (resto !== parseInt(cpf.charAt(10))) {
    classe_cpf.setAttribute("data-invalid", "true");
  }
}

function formatarTexto() {
  const textArea = document.getElementById('floatingTipoAtividade');
  const larguraCampo = textArea.clientWidth;
  const textoSemQuebras = textArea.value.replace(/\n/g, '');
  const textoFormatado = formatar(textoSemQuebras, larguraCampo);
  textArea.value = textoFormatado;
}

function formatar(texto, larguraCampo) {
  const caracteresPorLinha = 40; // Define o número de caracteres por linha
  let textoFormatado = '';

  for (let i = 0; i < texto.length; i += caracteresPorLinha) {
    textoFormatado += texto.substr(i, caracteresPorLinha) + '\n';
  }

  return textoFormatado;
}


